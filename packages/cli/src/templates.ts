function nexpressConfigTemplate(config: TemplateConfig): string {
  const imports = config.includeExampleContent
    ? 'import { postsCollection } from "./collections/posts";\nimport { pagesCollection } from "./collections/pages";\n\n'
    : "";
  const collections = config.includeExampleContent
    ? "[postsCollection, pagesCollection]"
    : "[]";
  // Storage is selected at runtime via NX_STORAGE_ADAPTER so the
  // operator can flip local ↔ S3 from `.env` without editing this
  // file. `pnpm run setup` writes the right env block directly.
  const storageConfig = `  storage:
    process.env.NX_STORAGE_ADAPTER === "s3"
      ? {
          adapter: "s3",
          s3: {
            bucket: process.env.NX_S3_BUCKET ?? "",
            region: process.env.NX_S3_REGION ?? "us-east-1",
            endpoint: process.env.NX_S3_ENDPOINT,
          },
        }
      : {
          adapter: "local",
          local: { directory: "./public/media", baseUrl: "/media" },
        },`;

  const pluginsHint = `  plugins: [\n    // Register sdk-built plugins here. Example:\n    //   import { readingTimePlugin } from "@nexpress/plugin-reading-time";\n    //   plugins: [readingTimePlugin],\n  ],`;

  return `import { defineConfig } from "@nexpress/core";\n${imports}export default defineConfig({\n  site: {\n    name: "${config.projectName}",\n    url: process.env.SITE_URL || "http://localhost:3000",\n  },\n  db: {\n    connectionString: process.env.DATABASE_URL!,\n  },\n${storageConfig}\n  collections: ${collections},\n  auth: {\n    secret: process.env.NX_SECRET!,\n  },\n${pluginsHint}\n});\n`;
}

