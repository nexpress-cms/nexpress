import type { ProjectConfig } from "./prompts.js";
import {
  readTemplate,
  walkTemplateTree,
  type TemplateFile,
} from "./template-loader.js";

type TemplateConfig = ProjectConfig & {
  secret: string;
  /**
   * Host port the scaffold's Postgres container binds to. Derived
   * from the project name by `dbPortFromProject` so two scaffolds
   * on the same machine get different defaults and `docker compose
   * up` doesn't collide on the previous-default 5433. Threaded
   * into `.env(.example)` for both `NEXPRESS_DB_PORT` (read by
   * the compose file's `${NEXPRESS_DB_PORT:-5433}` interpolation)
   * and the `DATABASE_URL` connection string.
   */
  dbPort: number;
};

function utf8(content: string): TemplateFile {
  return { encoding: "utf8", content };
}

export function getProjectFiles(config: TemplateConfig): Record<string, TemplateFile> {
  const files: Record<string, TemplateFile> = {
    ".env.example": utf8(envExampleTemplate(config)),
    ".env": utf8(envTemplate(config)),
    ".gitignore": utf8(gitignoreTemplate()),
    "README.md": utf8(readmeTemplate(config)),
    "drizzle.config.ts": utf8(drizzleConfigTemplate()),
    "next.config.ts": utf8(nextConfigTemplate()),
    "package.json": utf8(packageJsonTemplate(config)),
    "postcss.config.mjs": utf8(postcssConfigTemplate()),
    "tsconfig.json": utf8(tsconfigTemplate()),
    "next-env.d.ts": utf8(nextEnvTemplate()),
    // Always emitted: harmless on non-Vercel hosts, and the
    // /api/internal/publish-scheduled cron is needed by anyone using
    // the scheduled-publish flow on Vercel. README explains setup.
    "vercel.json": utf8(vercelJsonTemplate()),
    "public/media/.gitkeep": utf8(""),
    "src/db/generated/.gitkeep": utf8(""),
    // Empty re-export so `import * as generatedSchema` in bootstrap typechecks
    // before the user runs `pnpm db:generate` for the first time. The real
    // generated file overwrites this and adds the per-collection drizzle tables.
    "src/db/generated/collections.ts": utf8(
      "// Auto-generated by `pnpm db:generate`. Replace with real tables after first run.\nexport {};\n",
    ),
    // Stub `documents.ts` — same role as collections.ts above. The
    // snapshot's `@/lib/...` files import `@/db/generated/documents`
    // for typed `findPosts` / `PostsDocument` / etc., which only
    // get filled in by `pnpm db:generate`. Shipping a minimal stub
    // (empty find* results, baseline document shapes for the
    // built-in collections) keeps the freshly scaffolded project
    // typechecking before the first DB generate.
    "src/db/generated/documents.ts": utf8(documentsStubTemplate()),
    "src/nexpress.config.ts": utf8(nexpressConfigTemplate(config)),
    "src/collections/categories.ts": utf8(categoriesCollectionTemplate()),
    "src/collections/pages.ts": utf8(pagesCollectionTemplate()),
    "src/collections/posts.ts": utf8(postsCollectionTemplate()),
    "src/collections/tags.ts": utf8(tagsCollectionTemplate()),
    "scripts/_load-env.ts": utf8(loadEnvScriptTemplate()),
    "scripts/dev-notice.ts": utf8(devNoticeScriptTemplate()),
    "scripts/doctor.ts": utf8(doctorScriptTemplate()),
    "scripts/generate-schema.ts": utf8(generateSchemaScriptTemplate()),
    "scripts/postinstall-notice.ts": utf8(postinstallNoticeScriptTemplate()),
    "scripts/seed-admin.ts": utf8(seedAdminScriptTemplate()),
    "scripts/seed-content.ts": utf8(seedContentScriptTemplate()),
    "scripts/setup-server.ts": utf8(setupServerScriptTemplate()),
    "scripts/run-migrations.ts": utf8(runMigrationsScriptTemplate()),
    "scripts/worker.ts": utf8(workerScriptTemplate()),
  };

  // Snapshot of `apps/web/src/{app,lib,i18n.config.ts,proxy.ts}` —
  // mirrored into the scaffold's `src/` so a freshly scaffolded
  // project's pages and routes resolve to byte-identical code as
  // the reference app through @nexpress/app's subpath exports. The
  // thin re-export wrappers in `src/app/**/(page|route|layout).tsx`
  // and the corresponding lib/ implementations are not authored
  // here — they're frozen copies of what apps/web ships, kept in
  // lockstep by `pnpm sync-snapshot`. Editing a snapshot file by
  // hand drifts your scaffold from the reference; edit the source
  // in apps/web instead and resync.
  const snapshot = walkTemplateTree("snapshot");
  for (const [rel, file] of Object.entries(snapshot)) {
    files[rel] = file;
  }

  if (config.dockerSetup) {
    files["docker/Dockerfile"] = utf8(dockerfileTemplate());
    files["docker/docker-compose.yml"] = utf8(dockerComposeTemplate(config));
    // .dockerignore must live at the build context root (project
    // root), not under docker/. Source file is colocated with the
    // other docker assets but emitted to the root.
    files[".dockerignore"] = utf8(dockerignoreTemplate());
  }

  return files;
}

// Family range for scaffolded `@nexpress/*` deps. `^0.1.0` =
// `>= 0.1.0 < 0.2.0` — pins to the 0.1 minor family. `latest` is
// tempting but creates a footgun: a stale `create-nexpress` would
// scaffold against a future breaking `@nexpress/core` whose API
// the scaffold's source templates haven't kept up with. The
// `fixed` group in `.changeset/config.json` keeps every
// `@nexpress/*` on the same `0.<minor>.x` patch in lockstep, so
// the lower bound never misses a member of the family.
//
// Bump this when the family crosses a minor boundary (0.1.x →
// 0.2.x, 0.2.x → 0.3.x, 1.0.0, …). Without the bump, `npm install`
// resolves to the previous minor's last patch (e.g. with the pin
// still at `^0.1.0` after the family went to 0.2.x, scaffolded
// sites silently install 0.1.6 — the lib code shipped to operators
// is one minor stale).
const SCAFFOLDED_NEXPRESS_RANGE = "^0.2.0";

