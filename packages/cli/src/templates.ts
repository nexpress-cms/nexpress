import type { ProjectConfig } from "./prompts.js";
import { readTemplate, walkTemplateTree, type TemplateFile } from "./template-loader.js";

type TemplateConfig = ProjectConfig & {
  secret: string;
  /**
   * Host port the scaffold's Postgres container binds to. Derived
   * from the project name by `dbPortFromProject` so two scaffolds
   * on the same machine get different defaults and `docker compose
   * up` doesn't collide on the previous-default 5433. Threaded
   * into `.env(.example)` for both `NEXPRESS_DB_PORT` (read by
   * the compose file's `${NEXPRESS_DB_PORT:-5433}` interpolation)
   * and the `DATABASE_URL` connection string.
   */
  dbPort: number;
};

function utf8(content: string): TemplateFile {
  return { encoding: "utf8", content };
}

