/**
 * Scaffold's `pnpm-workspace.yaml`. The scaffold starts as a single app,
 * but it reserves `packages/plugins/*` and `packages/themes/*` for local
 * extension development. pnpm 10.6+ reads this file for `allowBuilds` (the new native-build
 * allowlist). The old `pnpm.onlyBuiltDependencies` block in `package.json`
 * is silently ignored in this context.
 *
 * NexPress hard-depends on three packages whose install scripts
 * compile native code: `sharp` (media transforms), `@node-rs/argon2`
 * (password hashing), and `esbuild` (transitive via tsx / vite /
 * next). Without these built, those features throw at runtime with
 * opaque "module not found" / "function not implemented" errors
 * deep inside the framework on the first request that exercises
 * them. Pre-approving them here makes a fresh `pnpm install`
 * complete without the operator needing to run `pnpm approve-builds`
 * — the warning the scaffolder's first-install ran into in
 * `npx create-nexpress my-site` reports today.
 *
 * Other native deps fall back to pnpm's safe default (ignored, with
 * an opt-in `pnpm approve-builds` flow if the operator pulls in a
 * dep that needs one).
 */
function pnpmWorkspaceYamlTemplate(): string {
  return `# pnpm 10.6+ native-build allowlist.
# Without these entries, a fresh \`pnpm install\` warns
# ERR_PNPM_IGNORED_BUILDS and the operator has to run
# \`pnpm approve-builds\` before any of the framework's
# native-backed features (image transforms / password hashing /
# bundle build) work. Approving them up front matches what
# \`pnpm approve-builds\` would write here anyway.
packages:
  - "packages/plugins/*"
  - "packages/themes/*"

allowBuilds:
  sharp: true
  "@node-rs/argon2": true
  esbuild: true
`;
}

function nexpressConfigTemplate(config: TemplateConfig): string {
  // Built-in collections + themes + i18n are sourced from
  // `@nexpress/app/config-defaults` so the single source of truth
  // lives in the framework, not in scaffolded boilerplate.
  // The four built-in themes + example collections + default plugins
  // are always wired in — the operator picks the active theme and
  // seeds (or skips) sample content in the first-boot admin setup
  // wizard, not at scaffold time.
  const collections = "[...defaultCollections]";
  // Marker pair lets `nexpress theme add <pkg>` /
  // `nexpress plugin add <pkg>` from `@nexpress/cli-nexpress`
  // append into the array later.
  const themes =
    "[\n    ...defaultThemes,\n    // @nexpress:themes-list-start\n    // @nexpress:themes-list-end\n  ]";
  const plugins =
    "[\n    ...defaultPlugins,\n    // @nexpress:plugins-list-start\n    // @nexpress:plugins-list-end\n  ]";
  const defaultsImports =
    'import {\n  defaultCollections,\n  defaultI18n,\n  defaultPlugins,\n  defaultThemes,\n  storageFromEnv,\n} from "@nexpress/app/config-defaults";\n';

  // Marker comments let `nexpress plugin add/remove <pkg>` and
  // `nexpress theme add <pkg>` from `@nexpress/cli-nexpress` edit
  // this file automatically. Keep them in place even when no
  // plugins or themes are installed — removing them forces the
  // operator to do manual edits later.
  const importsBlock =
    `// @nexpress:plugins-imports-start\n// @nexpress:plugins-imports-end\n` +
    `// @nexpress:themes-imports-start\n// @nexpress:themes-imports-end\n\n`;
  const pluginsList = `  plugins: ${plugins},`;

  return (
    `import { defineConfig } from "@nexpress/core";\n` +
    defaultsImports +
    `\n` +
    importsBlock +
    `export default defineConfig({\n` +
    `  site: {\n` +
    `    name: "${config.projectName}",\n` +
    `    url: process.env.SITE_URL || "http://localhost:3000",\n` +
    `  },\n` +
    `  db: {\n` +
    `    connectionString: process.env.DATABASE_URL!,\n` +
    `  },\n` +
    `  storage: storageFromEnv(),\n` +
    `  collections: ${collections},\n` +
    `  themes: ${themes},\n` +
    `  i18n: defaultI18n,\n` +
    `  auth: {\n` +
    `    secret: process.env.NP_SECRET!,\n` +
    `  },\n` +
    pluginsList +
    `\n});\n`
  );
}

